public class IfElse {

    public static void main(String[] args) {
        int t = 20;

        if (t < 18) {
            System.out.println("yes");
        } else {
            System.out.println("no");
        }

        int p = 90;

        int result = (p > 55) ? 90 : 55;
        System.out.println(result);
    }

}
